color = input("Elige un color entre: rojo, verde, azul, amarillo, morado: ")
color = color.lower()

if (color == "rojo"):
    print("El rojo es el color de la pasión y la energía. ¡Hoy será un día lleno de acción y emoción! No temas a los desafíos, saldrás victorioso.")
elif (color == "verde"):
    print("El verde representa la esperanza y el crecimiento. Algo nuevo y positivo está por florecer en tu vida. Confía en los pequeños cambios que te acercan a tus metas.")
elif (color == "azul"):
    print("El azul simboliza la calma y la serenidad. Hoy estarás rodeado de tranquilidad y equilibrio. Aprovecha este momento para reflexionar y renovar tu energía.")
elif (color == "amarillo"):
    print("El amarillo es el color de la felicidad y el optimismo. ¡Prepárate para un día lleno de alegría y buenas noticias! Alguien cercano te sorprenderá de forma positiva.")
elif (color == "morado "):
    print("El morado evoca la sabiduría y la creatividad. Hoy te sentirás inspirado y lleno de ideas. Es el momento ideal para dejar volar tu imaginación y tomar decisiones importantes.")
else:
    print("Ese color no es valido, vuelva a intentarlo")